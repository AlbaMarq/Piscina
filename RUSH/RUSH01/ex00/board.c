#include <unistd.h>

int	board(void)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			write(1, "* ", 2);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
